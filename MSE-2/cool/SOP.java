package cool;

public class SOP{
    public SOP(int a) {
        System.out.println(a);
    }

    public SOP(float a) {
        System.out.println(a);
    }

    public SOP(String a) {
        System.out.println(a);
    }

    public SOP(Object a) {
        System.out.println(a);
    }

    public SOP(char a) {
        System.out.println(a);
    }
}
